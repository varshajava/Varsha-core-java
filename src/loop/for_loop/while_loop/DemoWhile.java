package loop.for_loop.while_loop;

public class DemoWhile {

    public static void main(String[] args) {

        //Increment while loop

        int a=0;
        while(a<5)
        {
            System.out.println("Hello");
            a++;
        }

        //Decrement while loop

        int i=10;
        while(i>=1)
        {
            System.out.println("Hello : "+i);
            i--;
        }
    }
}
